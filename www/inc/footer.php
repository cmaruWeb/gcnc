    <script>
        document.addEventListener("DOMContentLoaded", function () {
            const dropdowns = document.querySelectorAll(".footer-link .dropdown");

            dropdowns.forEach(dropdown => {
                const btn = dropdown.querySelector(".dropdown-btn");
                const content = dropdown.querySelector(".dropdown-content");

                btn.addEventListener("click", function (e) {
                    e.stopPropagation();

                    // 다른 열린 메뉴 닫기
                    document
                        .querySelectorAll(".footer-link .dropdown-content")
                        .forEach(dc => {
                            if (dc !== content) 
                                dc.style.display = "none";
                            }
                        );
                    document
                        .querySelectorAll(".footer-link .dropdown-btn")
                        .forEach(b => {
                            if (b !== btn) 
                                b
                                    .classList
                                    .remove("active");
                            }
                        );

                    // 현재 메뉴 토글
                    const isVisible = content.style.display === "block";
                    content.style.display = isVisible
                        ? "none"
                        : "block";
                    btn
                        .classList
                        .toggle("active", !isVisible);
                });
            });

            // 바깥 클릭 시 메뉴 닫기
            document.addEventListener("click", function () {
                document
                    .querySelectorAll(".footer-link .dropdown-content")
                    .forEach(dc => dc.style.display = "none");
                document
                    .querySelectorAll(".footer-link .dropdown-btn")
                    .forEach(b => b.classList.remove("active"));
            });
        });
    </script>

